  /**
   * 轉換簡體中文為繁體中文
   */
  async convertPromise(text) {
    return this.convert(text);
  }

  /**
   * 同步轉換方法
   */
  convert(text) {
    if (!text || typeof text !== 'string') {
      return text;
    }

    let result = text;
    
    // 按鍵長度排序，優先轉換長詞組
    const sortedEntries = Array.from(this.conversionMap.entries())
      .sort((a, b) => b[0].length - a[0].length);
    
    for (const [simplified, traditional] of sortedEntries) {
      if (result.includes(simplified)) {
        // 使用全局替換
        const regex = new RegExp(this.escapeRegExp(simplified), 'g');
        result = result.replace(regex, traditional);
      }
    }
    
    return result;
  }

  /**
   * 轉義正則表達式特殊字符
   */
  escapeRegExp(string) {
    return string.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  }

  /**
   * 檢測是否包含簡體中文
   */
  containsSimplified(text) {
    if (!text || typeof text !== 'string') {
      return false;
    }

    // 檢查是否包含任何簡體字
    for (const simplified of this.conversionMap.keys()) {
      if (text.includes(simplified)) {
        return true;
      }
    }
    
    return false;
  }

  /**
   * 獲取轉換統計
   */
  getConversionStats(originalText, convertedText) {
    const conversions = [];
    
    for (const [simplified, traditional] of this.conversionMap.entries()) {
      if (originalText.includes(simplified)) {
        const count = (originalText.match(new RegExp(this.escapeRegExp(simplified), 'g')) || []).length;
        conversions.push({
          simplified,
          traditional,
          count
        });
      }
    }
    
    return {
      totalConversions: conversions.length,
      conversions,
      hasChanged: originalText !== convertedText,
      dictionarySize: this.conversionMap.size
    };
  }
